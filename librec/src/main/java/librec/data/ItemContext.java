package librec.data;

/**
 * Item-related Contextual Information
 * 
 * @author guibing
 * 
 */
public class ItemContext extends Context {

	private String category;

	/**
	 * @param user
	 *            fixed as -1
	 * @param item
	 *            item id
	 */
	public ItemContext(int user, int item) {
		super(user, item);
	}

	/**
	 * @return the category
	 */
	public String getCategory() {
		return category;
	}

	/**
	 * @param category
	 *            the category to set
	 */
	public void setCategory(String category) {
		this.category = category;
	}

}
